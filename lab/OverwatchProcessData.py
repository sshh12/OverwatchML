def get_competitive_rank(player, region):
    
    try:

        return int(player.json[region]['stats']['competitive']['overall_stats']['comprank'])

    except (TypeError, KeyError):

        return False


# In[3]:


def get_vector_gamestats(player, region, gametype="competitive", stat_keys=None):
    
    if not stat_keys:
            
        stat_keys = general_stats
        
    try:

        stats = player.json[region]['stats'][gametype]['game_stats']
            
        vector = []
            
        for key in stat_keys:
                
            if 'overwatchguid' not in key:
                    
                if key not in stats:
                        
                    vector.append(0)
                        
                else:
                
                    vector.append(stats[key])
                
        return np.array(vector)

    except (TypeError, KeyError):

        return False


# In[4]:


def get_vector_herostats(player, region, gametype='competitive', stat_keys=None):
    
    if not stat_keys:
        
        stat_keys = hero_stats
        
    try:

        stats = player.json[region]['heroes']['stats'][gametype]
            
        vector = []
            
        for stat in stat_keys:
            
            hero, stat_type, value = stat.split(" ")

            try:

                vector.append(stats[hero][stat_type][value])

            except (TypeError, KeyError):

                vector.append(0)
                
        return np.array(vector)

    except (TypeError, KeyError):

        return False


# In[5]:


def get_vector_combined(player, region, gametype='competitive', stat_keys_general=None, stat_keys_heros=None):
    
    if not stat_keys_general:
        
        stat_keys_general = general_stats
        
    if not stat_keys_heros:
        
        stat_keys_heros = hero_stats
        
    vector_1 = get_vector_gamestats(player, region, gametype, stat_keys=stat_keys_general)
    vector_2 = get_vector_herostats(player, region, gametype, stat_keys=stat_keys_heros)
    
    try:
        
        return np.concatenate([vector_1, vector_2])
    
    except:
    
        return False


