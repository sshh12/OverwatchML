def print_hero_stat_list():
    
    import json
    
    hero_stats = []
    
    for filename in os.listdir(os.path.join('..', 'profiles')):
        
        with open(os.path.join('..', 'profiles', filename), 'r') as profile:

            player_json = profile.read()
        
        if 'error' not in player_json:
            
            try:
                
                player_json = json.loads(player_json)
            
                stats = player_json['us']['heroes']['stats']['competitive']

                for hero in stats:

                    for stat_type in stats[hero]:

                        for stat in stats[hero][stat_type]:
                            
                            stat_string = '{} {} {}'.format(hero, stat_type, stat)
                            
                            if stat_string not in hero_stats:

                                hero_stats.append(stat_string)
                            
            except:
                pass
        
    hero_stats.sort()
                        
    for stat in hero_stats:
        
        print("'" + stat + "'")

